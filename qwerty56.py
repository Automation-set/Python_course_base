n = int(input()) # Количество матчей
sl = {}
y = []
while n != 0:
    n -= 1
    x = input().split(';')
    # Если в словаре нет такой команды, добавляем ее название как ключ
    if x[0] not in sl:
        sl[x[0]] = [0, 0, 0, 0]
    if x[2] not in sl:
        sl[x[2]] = [0, 0, 0, 0]
    # Сравниваем кто победил в матче
    if int(x[3]) == int(x[1]):
        sl[x[0]] = [+1, 0, +1, 0]
        sl[x[2]] = [+1, 0, +1, 0]

    y = x
print(y)
print(sl)